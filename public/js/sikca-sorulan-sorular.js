const icons = document.querySelectorAll(".icon");
const optionContent = document.querySelectorAll(".collapse-wrap-item-content");

document.addEventListener("click", function (event) {
  let target = event.target;

  if (target.classList.contains("collapse-wrap-item")) {
    let content = target.querySelector(".collapse-wrap-item-content");
    let icon = target.querySelector(".icon");

    let active = content.classList.contains("active");

    if (active) {
      content.classList.remove("active");
      icon.classList.remove("active");
    } else {
      content.classList.add("active");
      icon.classList.add("active");
    }
  }
});
